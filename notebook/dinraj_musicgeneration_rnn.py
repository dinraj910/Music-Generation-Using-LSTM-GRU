"""## 📝 Section 13 — Limitations & What Transformers Solve

This section is for your README, blog post, and interview answers.

### What This RNN Model Does Well
- Learns **local melodic patterns** (which note likely follows which)
- Generates sequences that are **tonally plausible** (stays roughly in-key)
- **Fast to train** — 50 epochs in minutes on T4
- **Small model** (~1M params) — can run on CPU in a web app

### Fundamental Limitations of RNN Music Models

| Problem | What Happens | Why |
|---------|-------------|-----|
| **Long-term structure** | No repeated themes, no verse/chorus | LSTM hidden state can't compress 200+ notes |
| **Repetition loops** | Same 4-note phrase repeats forever | High-prob cycles dominate at low temperature |
| **No hierarchy** | Can't distinguish bar vs phrase vs section | Flat token sequence = no structural tokens |
| **Pitch-only** | Rhythm sounds mechanical | Duration not in vocabulary |
| **No harmony** | Single melody, no chords | Chord modeling needs simultaneous tokens |

### How Transformers (Music Transformer, MusicGen) Solve These

```
RNN:          token_t = f(token_{t-1}, hidden_{t-1})
              ↑ can only look back through a compressed vector

Transformer:  token_t = attention(token_t, ALL previous tokens)
              ↑ direct attention to any past position
              ↑ bar 1 theme can directly influence bar 32
              ↑ relative position embeddings encode musical timing
```

**Your next project upgrade path:**
1. Add duration tokens → rhythmically expressive output
2. Replace LSTM with small Transformer decoder → long-range structure  
3. Add instrument conditioning → generate for specific instruments
4. Fine-tune on genre-specific data → stylistic control

---

> **Portfolio Note:** The fact that you can articulate *why* RNNs fail at global
> musical structure — and explain how attention mechanisms fix it — demonstrates
> the kind of architectural understanding that ₹25L+ AI roles test for.

"""