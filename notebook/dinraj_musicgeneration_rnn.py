"""## 🧠 Section 6 — Model Architectures

### Architecture Overview

| Model | Type | Can Generate? | Params | Notes |
|-------|------|:---:|--------|-------|
| **StackedLSTM** | Unidirectional | ✅ Yes | ~1.1M | Production generator |
| **StackedGRU** | Unidirectional | ✅ Yes | ~850K | Faster, comparable quality |
| **BiLSTM** | Bidirectional | ❌ No | ~2.1M | Classifier only — explains WHY |

> ⚠️ **BiLSTM cannot generate music.** During training it reads future notes (backward pass).
> During generation, future notes don't exist yet. It's kept here to demonstrate this
> concept empirically — BiLSTM gets *better* validation loss but *cannot* compose music.

"""

# ── Model factory ───────────────────────────────────────────────────

def build_stacked_lstm(name='StackedLSTM'):
    """
    Two-layer stacked LSTM.
    Autoregressive generation: ✅ YES

    Architecture:
      Input(SEQ_LEN) → Embedding(89,64) → LSTM(256, return_seq=True)
      → Dropout(0.3) → LSTM(256) → Dropout(0.3) → Dense(89)
    """
    inp       = keras.Input(shape=(CFG['SEQ_LEN'],), name='token_input')
    x         = layers.Embedding(CFG['VOCAB_SIZE'], CFG['EMBED_DIM'],
                                  name='embedding')(inp)
    # Layer 1: return_sequences=True so Layer 2 gets full seq
    x         = layers.LSTM(CFG['HIDDEN_DIM'], return_sequences=True,
                             name='lstm_1')(x)
    x         = layers.Dropout(CFG['DROPOUT'], name='drop_1')(x)
    # Layer 2: return_sequences=False — we only need last timestep
    x         = layers.LSTM(CFG['HIDDEN_DIM'], return_sequences=False,
                             name='lstm_2')(x)
    x         = layers.Dropout(CFG['DROPOUT'], name='drop_2')(x)
    out       = layers.Dense(CFG['VOCAB_SIZE'], activation='softmax',
                              name='output')(x)
    model = keras.Model(inp, out, name=name)
    return model


def build_stacked_gru(name='StackedGRU'):
    """
    Two-layer stacked GRU.
    Autoregressive generation: ✅ YES

    GRU vs LSTM:
      - GRU has 2 gates (reset, update) vs LSTM's 4 gates
      - ~22% fewer parameters
      - Trains ~25% faster per epoch
      - No separate cell state — merged into hidden state
      - Often matches LSTM for SEQ_LEN=32 (not long enough for LSTM to shine)
    """
    inp  = keras.Input(shape=(CFG['SEQ_LEN'],), name='token_input')
    x    = layers.Embedding(CFG['VOCAB_SIZE'], CFG['EMBED_DIM'],
                             name='embedding')(inp)
    x    = layers.GRU(CFG['HIDDEN_DIM'], return_sequences=True,
                      name='gru_1')(x)
    x    = layers.Dropout(CFG['DROPOUT'], name='drop_1')(x)
    x    = layers.GRU(CFG['HIDDEN_DIM'], return_sequences=False,
                      name='gru_2')(x)
    x    = layers.Dropout(CFG['DROPOUT'], name='drop_2')(x)
    out  = layers.Dense(CFG['VOCAB_SIZE'], activation='softmax',
                         name='output')(x)
    model = keras.Model(inp, out, name=name)
    return model


def build_bilstm(name='BidirectionalLSTM'):
    """
    Two-layer Bidirectional LSTM — CLASSIFIER ONLY.
    Autoregressive generation: ❌ NO

    WHY it cannot generate:
      Bidirectional runs TWO passes:
        Forward:  reads notes left → right (past context)
        Backward: reads notes right → left (FUTURE context)
      At training time, all 32 window notes are available → backward
      pass works fine. At generation time, future notes DON'T EXIST YET
      → backward pass has nothing to read → cannot run.

    The output dim doubles: 256 forward + 256 backward = 512 per layer.
    This is why it gets better val_loss than unidirectional — it's
    'cheating' by seeing the future during training.
    """
    inp  = keras.Input(shape=(CFG['SEQ_LEN'],), name='token_input')
    x    = layers.Embedding(CFG['VOCAB_SIZE'], CFG['EMBED_DIM'],
                             name='embedding')(inp)
    # Bidirectional wrapper doubles output dim: 256*2 = 512
    x    = layers.Bidirectional(
               layers.LSTM(CFG['HIDDEN_DIM'], return_sequences=True),
               name='bilstm_1')(x)
    x    = layers.Dropout(CFG['DROPOUT'], name='drop_1')(x)
    x    = layers.Bidirectional(
               layers.LSTM(CFG['HIDDEN_DIM'], return_sequences=False),
               name='bilstm_2')(x)
    x    = layers.Dropout(CFG['DROPOUT'], name='drop_2')(x)
    out  = layers.Dense(CFG['VOCAB_SIZE'], activation='softmax',
                         name='output')(x)
    model = keras.Model(inp, out, name=name)
    return model


