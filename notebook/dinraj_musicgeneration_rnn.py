"""## 🏋️ Section 7 — Training All 3 Models

**Callbacks used:**
- `ModelCheckpoint` — saves best model (val_loss)
- `EarlyStopping` — stops if val_loss doesn't improve for 8 epochs
- `ReduceLROnPlateau` — halves LR after 4 epochs no improvement
- `CSVLogger` — logs all metrics to file for post-analysis

**Expected training time on T4 GPU:** ~2-4 minutes per model (50 epochs max)

"""

# ── Training function ───────────────────────────────────────────────

history_dict = {}  # stores training history for all 3 models

def train_model(model, model_name, train_ds, val_ds):
    print(f'\n{"="*60}')
    print(f'Training: {model_name}')
    print(f'{"="*60}')

    ckpt_path = os.path.join(CFG['EXPORT_DIR'], f'{model_name}_best.keras')
    log_path  = os.path.join(CFG['EXPORT_DIR'], f'{model_name}_log.csv')

    cbs = [
        callbacks.ModelCheckpoint(
            filepath=ckpt_path,
            monitor='val_loss',
            save_best_only=True,
            verbose=0
        ),
        callbacks.EarlyStopping(
            monitor='val_loss',
            patience=CFG['PATIENCE'],
            restore_best_weights=True,
            verbose=1
        ),
        callbacks.ReduceLROnPlateau(
            monitor='val_loss',
            factor=0.5,
            patience=4,
            min_lr=1e-6,
            verbose=1
        ),
        callbacks.CSVLogger(log_path)
    ]

    history = model.fit(
        train_ds,
        epochs          = CFG['EPOCHS'],
        validation_data = val_ds,
        callbacks       = cbs,
        verbose         = 1
    )

    print(f'\n✅ {model_name} done.')
    print(f'   Best val_loss : {min(history.history["val_loss"]):.4f}')
    print(f'   Best val_acc  : {max(history.history["val_accuracy"]):.4f}')
    return history


