"""## 🎼 Section 10 — Music Generation

### How Autoregressive Generation Works

```
seed: [C4, E4, G4, ...(32 notes)]
         ↓
    LSTM forward pass
         ↓
    softmax → probabilities over 89 tokens
         ↓
    temperature sampling → sample next token
         ↓
    append to sequence, drop oldest
         ↓
    repeat → 200 notes generated
```

### Temperature Parameter
| Temperature | Effect | Result |
|-------------|--------|--------|
| `0.5` | Conservative | Repetitive but in-key |
| `0.8` | Balanced | Recommended |
| `1.0` | Standard softmax | More variety |
| `1.2` | Creative | Surprising, less tonal |
| `2.0` | Chaotic | Random noise |

"""

# ── Temperature sampling function ───────────────────────────────────

def sample_with_temperature(logits, temperature=0.8):
    """
    Apply temperature scaling to logits, then sample.

    Temperature formula:
        scaled_logits = log(probs) / temperature
        sample from softmax(scaled_logits)

    Low  temperature → sharper distribution → more deterministic
    High temperature → flatter distribution → more random

    Why not just argmax (greedy)?
        Greedy decoding quickly gets stuck in loops:
        C4 → E4 → G4 → C4 → E4 → G4 → ...
        Temperature adds controlled randomness that breaks loops.
    """
    if temperature == 0:
        return int(np.argmax(logits))

    logits = np.log(np.array(logits, dtype=np.float64) + 1e-10)
    logits = logits / temperature
    # Subtract max for numerical stability before exp
    logits = logits - np.max(logits)
    probs  = np.exp(logits)
    probs  = probs / np.sum(probs)
    return int(np.random.choice(len(probs), p=probs))


def generate_sequence(model, seed_tokens, length=200, temperature=0.8):
    """
    Autoregressively generate `length` new tokens.

    seed_tokens : list of SEQ_LEN integer tokens to prime the model
    Returns     : list of generated integer tokens (length = `length`)
    """
    generated  = []
    window     = list(seed_tokens[-CFG['SEQ_LEN']:])

    for step in range(length):
        # Shape: (1, SEQ_LEN)
        x     = np.array(window, dtype=np.int32).reshape(1, -1)
        probs = model.predict(x, verbose=0)[0]  # (89,) probabilities

        # Sample next token with temperature
        next_token = sample_with_temperature(probs, temperature)

        generated.append(next_token)
        window.append(next_token)
        window.pop(0)  # slide window forward

        if (step + 1) % 50 == 0:
            print(f'  Generated {step+1}/{length} tokens...', end='\r')

    print(f'  Generated {length}/{length} tokens ✅  ')
    return generated


