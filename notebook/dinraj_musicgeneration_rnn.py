# ── MIDI parser ──────────────────────────────────────────────────────

def parse_score(score_or_path, use_corpus=True):
    """
    Parse a music21 score or MIDI path → list of pitch tokens.

    Steps:
      1. Load score (corpus or file)
      2. Flatten to single stream of events
      3. Extract notes and chords sorted by onset offset
      4. Chords → take highest pitch (melody preservation)
      5. Insert REST tokens for gaps > REST_THRESHOLD
      6. Filter to piano range [MIDI_MIN, MIDI_MAX]

    Returns: list[int] of tokens, or [] if parsing fails.
    """
    try:
        if use_corpus:
            score = corpus.parse(score_or_path)
        else:
            score = converter.parse(score_or_path)
    except Exception as e:
        return []

    try:
        notes_flat = score.flat.notes
    except Exception:
        return []

    extracted = []  # (offset, midi_pitch)

    for element in notes_flat:
        try:
            offset = float(element.offset)
            if isinstance(element, note.Note):
                extracted.append((offset, element.pitch.midi))
            elif isinstance(element, chord.Chord):
                # Take highest pitch = melody note
                top_pitch = max(n.midi for n in element.pitches)
                extracted.append((offset, top_pitch))
        except:
            continue

    if len(extracted) < CFG['SEQ_LEN'] + 5:
        return []

    # Sort by onset time
    extracted.sort(key=lambda x: x[0])

    # Filter to piano range
    extracted = [(o, p) for o, p in extracted
                 if CFG['MIDI_MIN'] <= p <= CFG['MIDI_MAX']]

    if not extracted:
        return []

    # Build token sequence with REST insertion
    tokens = []
    prev_offset = extracted[0][0]

    for offset, pitch in extracted:
        gap = offset - prev_offset
        if gap > CFG['REST_THRESHOLD']:
            tokens.append(CFG['REST_TOKEN'])
        tokens.append(pitch_to_token(pitch))
        prev_offset = offset

    return tokens


