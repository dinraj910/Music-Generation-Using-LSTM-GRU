"""## 🔧 Section 4 — MIDI Preprocessing & Tokenization

**Token Vocabulary (89 tokens):**
- Token `0–87` → MIDI pitches 21–108 (full piano range)
- Token `88` → REST (silence between notes)

**Chord handling:** Take the HIGHEST pitch (preserves melody line)

**REST insertion:** Gap > 0.5 quarter-notes between notes → insert REST token
"""

# ── Token helper functions ───────────────────────────────────────────

def pitch_to_token(midi_pitch):
    """MIDI pitch 21-108 → token 0-87."""
    return int(midi_pitch) - CFG['MIDI_MIN']

def token_to_pitch(token):
    """Token 0-87 → MIDI pitch 21-108."""
    return token + CFG['MIDI_MIN']

def token_to_name(token):
    """Token → human readable note name (e.g. token 39 → 'C4')."""
    if token == CFG['REST_TOKEN']:
        return 'REST'
    midi_pitch = token_to_pitch(token)
    NOTE_NAMES = ['C','C#','D','D#','E','F','F#','G','G#','A','A#','B']
    octave = (midi_pitch // 12) - 1
    name   = NOTE_NAMES[midi_pitch % 12]
    return f'{name}{octave}'

