# ── Tokens → MIDI file ──────────────────────────────────────────────

def tokens_to_midi(tokens, output_path, tempo_bpm=120, note_duration=0.25):
    """
    Convert pitch token list → MIDI file.

    Since we used pitch-only tokenization (no duration info),
    every non-REST note gets a fixed duration of `note_duration` seconds.
    REST tokens add silence of the same duration.

    This is an inherent limitation of pitch-only representation —
    the output sounds metronomic. Adding duration tokens would fix this
    (next iteration of the model).

    Args:
        tokens       : list of int token indices
        output_path  : path to save .mid file
        tempo_bpm    : beats per minute
        note_duration: seconds per note (uniform)
    """
    midi = pretty_midi.PrettyMIDI(initial_tempo=tempo_bpm)
    piano = pretty_midi.Instrument(
        program=pretty_midi.instrument_name_to_program('Acoustic Grand Piano')
    )

    current_time = 0.0

    for token in tokens:
        if token == CFG['REST_TOKEN']:
            # REST: advance time, no note
            current_time += note_duration
        else:
            midi_pitch = token_to_pitch(token)
            # Clip to valid MIDI range just in case
            midi_pitch = max(0, min(127, midi_pitch))

            note_on  = current_time
            note_off = current_time + note_duration * 0.9  # 10% gap between notes

            note_obj = pretty_midi.Note(
                velocity  = 80,
                pitch     = midi_pitch,
                start     = note_on,
                end       = note_off
            )
            piano.notes.append(note_obj)
            current_time += note_duration

    midi.instruments.append(piano)
    midi.write(output_path)
    duration = current_time
    return duration


